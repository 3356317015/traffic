			JSONObject jsonConfig = JSONObject.fromObject(strConfig);
			Map mapConfig = (Map)jsonConfig;
	        //3.调用业务
			EpdFaresuit newEpdFaresuit = iNetEpdFaresuit.insert(epdFaresuit, mapConfig);
